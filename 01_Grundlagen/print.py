# Ausgabe von Text und Zahlen
print("Hallo Welt")  # Gibt den Text "Hallo Welt" aus
print(100)           # Gibt die Zahl 100 aus

# Verwendung von f-Strings zur Formatierung von Text
name = "Hans"
alter = 25
print(f"{name} ist {alter} Jahre alt.")  # Gibt formatierten Text aus, z.B. "Hans ist 25 Jahre alt."

# Verwendung von f-Strings zur Einbindung von Variablenwerten
x = 10
print(f"Der Wert von x ist: {x}")  # Gibt den Wert von x aus, z.B. "Der Wert von x ist: 10"

# Definition einer Liste
meine_liste = [1, 2, 3]

# Ausgabe des Inhalts der Liste
print("Inhalt der Liste:", meine_liste)  # Gibt den Text "Inhalt der Liste:" gefolgt von der Liste aus
