a = 5
b = 10
c = 15

# Vergleiche, ob a gleich b ist
print(a == b)  # Gibt False aus, da 5 nicht gleich 10 ist

# Vergleiche, ob a ungleich b ist
print(a != b)  # Gibt True aus, da 5 nicht gleich 10 ist.

# Vergleiche, ob a größer als b ist
print(a > b)  # Gibt False aus, da 5 nicht größer als 10 ist

# Vergleiche, ob a kleiner als b ist
print(a < b)  # Gibt True aus, da 5 kleiner als 10 ist

# Vergleiche, ob a größer oder gleich b ist
print(a >= b)  # Gibt False aus, da 5 nicht größer oder gleich 10 ist

# Vergleiche, ob a kleiner oder gleich b ist
print(a <= b)  # Gibt True aus, da 5 kleiner oder gleich 10 ist

# Bedingung: Wenn a kleiner als b und b größer als c ist, dann gebe den Text aus
if a < b and b > c:
    print("b ist größer als a und c")



